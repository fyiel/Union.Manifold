import { DEFAULT_FONT_MONO, DEFAULT_FONT_SANS } from "./fonts"
import { THEME_SCHEMA_VERSION, type ThemeColors, type ThemeDef } from "./types"

function preset(id: string, name: string, colors: ThemeColors, opts?: { radius?: string; fontSans?: string; fontMono?: string }): ThemeDef {
  return {
    schemaVersion: THEME_SCHEMA_VERSION,
    id,
    name,
    source: "preset",
    colors,
    radius: opts?.radius ?? "0.625rem",
    fontSans: opts?.fontSans ?? DEFAULT_FONT_SANS,
    fontMono: opts?.fontMono ?? DEFAULT_FONT_MONO,
  }
}

